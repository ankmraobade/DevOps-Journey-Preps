#!/usr/local/bin/python3

#########################################################################################################
# Definite loops using 'for'                                                                            #
#########################################################################################################

cities = ['Hyderabad', 'Chennai', 'Bangalore', 'Pune']  # cities 'variable' with 'list' 

for city in cities:             # city is the iteration variable in the loop
    print('Welcome to',city)
print('\nDone!')


