#!/usr/local/bin/python3

#########################################################################################################
# Iterations - using while loop                                                                         #
#########################################################################################################

n = 5                   # initializing variable

while n > 0:            # condition set to n > 0, means - display the value of n until n is > 0
    n = n - 1           # each time decrement by 1
    print(n)
    
print("Blastoff!")  